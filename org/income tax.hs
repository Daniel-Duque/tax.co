-- ingresos ocasionales

renta gravable ocasional, 10%-taxable =
  long-term asset sales
  + inheritance
  + f (donations from private firms)
  where f x = x - min (20% x, 2290 uvt)

renta gravable ocasional, 20%-taxable =
  gambling
  + jury awards
