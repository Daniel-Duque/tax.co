A dependent counts for 10% of the applicable renta liquida.

-- Common
ing. no constitutivo de renta =
  SS contributions
  + GMF / 2
  + things we don't have information about

-- Labor + pension income
Renta liquida laboral = ingreso laboral
  - ing laboral no constitutivo de renta
Renta gravable laboral = renta liquida laboral
  - f beneficios
  where f x = min( 40% renta gravable laboral
                 , 5040 uvt)

Renta gravable pension = ingreso pension
  - ingreso pension no constitutivo de renta
  - renta exenta hasta mil uvt

Impuesto tarifa uno aplica a:
  renta gravable (pension + laboral) 

-- Capital + nonlabor income
Renta liquida capital = ingreso capital
  - ingreso capital no constitutivo de renta
  where ingreso capital = interest
          + arriendos (literal rent, not profit)
          + regalias
        ing K no constitutivo de renta = unknowable

Renta gravable de capital = renta liquida capital
                            - f beneficios
  where f x = min ( 10 pct of renta gravable de capital
                  , 1000 uvt )

Renta liquida no laboral = ingreso no laboral
  - ingreso no laboral no constiotutivo de renta
  where ingreso no laboral = short-term sales
        ingreso no laboral no constiotutivo de renta = unknowable

Renta gravable no laboral = renta liquida no laboral
  - f beneficios
  where f x = min ( 10 pct of renta gravable de capital
                  , 1000 uvt )

Tarifa 2 applies to: 
  Renta gravable (capital + no laboral)

